type I18nCtx = {
  lang: Language;
  setLang: (l: Language) => void;
  t: (key: string) => string;
  dir: "ltr" | "rtl";
};

const Ctx = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Language>("en");

  // react-doctor-disable-next-line react-doctor/no-initialize-state
  useEffect(() => {
    if (typeof window === "undefined") return;
    const stored = localStorage.getItem("bq.lang") as Language | null;
    // react-doctor-disable-next-line react-doctor/no-initialize-state
    if (stored === "en" || stored === "ur") setLangState(stored);
  }, []);

  // react-doctor-disable-next-line react-doctor/no-effect-chain
  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ur" ? "rtl" : "ltr";
  }, [lang]);

  const setLang = (l: Language) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("bq.lang", l);
  };

  const t = (key: string) => dictionaries[lang][key] ?? dictionaries.en[key] ?? key;

  const ctxValue = useMemo(
    () => ({ lang, setLang, t, dir: lang === "ur" ? "rtl" : "ltr" }) as I18nCtx,
    // eslint-disable-next-line react-hooks/exhaustive-deps -- t is derived from lang; including it causes double-memo since t is recreated on every render
    [lang],
  );

  return <Ctx.Provider value={ctxValue}>{children}</Ctx.Provider>;
}

// eslint-disable-next-line react-refresh/only-export-components -- standard context+hook co-location
export function useI18n() {
  const ctx = use(Ctx);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
